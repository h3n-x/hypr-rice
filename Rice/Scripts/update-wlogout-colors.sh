# Funci√≥n para verificar dependencias de iconos
check_icons() {
  local missing_icons=()
  local icons=("lock.svg" "logout.svg" "suspend.svg" "hibernate.svg" "shutdown.svg" "reboot.svg")

  echo "üîç Verificando iconos en $WLOGOUT_ICONS_DIR..."

  for icon in "${icons[@]}"; do
    if [ ! -f "$WLOGOUT_ICONS_DIR/$icon" ]; then
      missing_icons+=("$icon")
    fi
  done

  if [ ${#missing_icons[@]} -gt 0 ]; then
    echo "‚ö†Ô∏è  Iconos faltantes:"
    for icon in "${missing_icons[@]}"; do
      echo "   - $icon"
    done
    echo ""
    echo "üí° Los iconos se buscar√°n en las siguientes ubicaciones:"
    echo "   - $WLOGOUT_ICONS_DIR/ (configurado)"
    echo "   - /usr/share/wlogout/icons/ (sistema)"
    echo "   - Fallback a color s√≥lido"
    echo ""
  else
    echo "‚úÖ Todos los iconos est√°n presentes:"
    for icon in "${icons[@]}"; do
      echo "   ‚úì $icon"
    done
  fi
}

